import 'package:flutter/material.dart';
import 'package:shared_preferences/shared_preferences.dart';

class LanguageProvider with ChangeNotifier {
  static const String _languageKey = 'app_language';
  static const String defaultLocale = 'zh_TW';

  Locale _currentLocale = const Locale('zh', 'TW');
  String _currentLanguageTag = defaultLocale;

  Locale get currentLocale => _currentLocale;
  String get currentLanguageCode => _currentLocale.languageCode;
  String get currentLanguageTag => _currentLanguageTag;

  Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    final savedLanguage = prefs.getString(_languageKey) ?? defaultLocale;
    _setLocale(savedLanguage);
  }

  Future<void> setLanguage(String languageTag) async {
    _setLocale(languageTag);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_languageKey, languageTag);
    notifyListeners();
  }

  void _setLocale(String languageTag) {
    _currentLanguageTag = languageTag;
    final parts = languageTag.split('_');
    if (parts.length >= 2) {
      _currentLocale = Locale(parts[0], parts[1]);
    } else {
      _currentLocale = Locale(parts[0]);
    }
  }

  String t(String key) {
    final translations = _translations[_currentLanguageTag] ?? _translations[defaultLocale]!;
    return translations[key] ?? key;
  }

  static String getLanguageName(String languageTag) {
    switch (languageTag) {
      case 'zh_TW': return '繁體中文';
      case 'zh_CN': return '简体中文';
      case 'en_US': return 'English';
      case 'fr_FR': return 'Français';
      case 'es_ES': return 'Español';
      case 'ru_RU': return 'Русский';
      case 'ar_SA': return 'العربية';
      default: return languageTag;
    }
  }

