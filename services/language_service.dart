    final enabledLanguages = [
      'zh_TW', 'zh_CN', 'en_US', 'fr_FR', 'es_ES', 'ru_RU', 'ar_SA'
    ];
    return enabledLanguages.contains(languageTag);
  }

  static bool isRTL(String languageTag) {
    return languageTag == 'ar_SA';
  }

  static TextDirection getTextDirection(String languageTag) {
    return isRTL(languageTag) ? TextDirection.rtl : TextDirection.ltr;
